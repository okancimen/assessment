'use client'

import { useState, useEffect, useRef } from 'react'
import { useRouter } from 'next/navigation'
import {
  InternshipSubject,
  InternshipTrack,
  INTERNSHIP_PHASE_LABELS,
  INTERNSHIP_QUESTIONS_PER_SUBJECT,
  getInternshipPhase,
  getInternshipSubjects,
  Question,
} from '@/types'
import QuestionCard from '@/components/assessment/QuestionCard'
import Button from '@/components/ui/Button'

const LOADING_FACTS = [
  'Did you know? This assessment uses adaptive technology — questions adjust to your level as you answer.',
  'Tip: For situational judgement questions, think about what a professional mentor would consider best practice.',
  'Fun fact: Domain knowledge questions test applied thinking, not memorisation — read each scenario carefully.',
  'Tip: The interest profile section has no right or wrong answers — answer honestly for the most accurate results.',
  'Did you know? Your readiness report is generated by AI that analyses your full performance across all four phases.',
  'Tip: Take your time — the 2-minute timer gives you plenty of space to think. Accuracy matters more than speed.',
  'Fun fact: Soft skills (workplace scenarios) are one of the strongest predictors of internship success.',
]

function LoadingQuestion() {
  const [factIndex, setFactIndex] = useState(0)
  const [fade, setFade] = useState(true)

  useEffect(() => {
    const timer = setInterval(() => {
      setFade(false)
      setTimeout(() => { setFactIndex((i) => (i + 1) % LOADING_FACTS.length); setFade(true) }, 400)
    }, 5000)
    return () => clearInterval(timer)
  }, [])

  return (
    <div className="min-h-screen bg-[#f5f5f7] flex items-center justify-center px-4">
      <div className="text-center space-y-6 max-w-sm">
        <div className="relative w-16 h-16 mx-auto">
          <div className="absolute inset-0 rounded-full border-4 border-[#e0e7ff]" />
          <div className="absolute inset-0 rounded-full border-4 border-[#4F46E5] border-t-transparent animate-spin" />
        </div>
        <div className="space-y-1.5">
          <p className="text-[#1d1d1f] font-semibold text-base">Preparing your next question</p>
          <p className="text-[#6e6e73] text-sm">AI is personalising your assessment in real time.</p>
        </div>
        <div
          className="bg-white border border-[#d2d2d7] rounded-2xl px-5 py-3.5 text-sm text-[#6e6e73] leading-relaxed transition-opacity duration-400"
          style={{ opacity: fade ? 1 : 0 }}
        >
          {LOADING_FACTS[factIndex]}
        </div>
      </div>
    </div>
  )
}

